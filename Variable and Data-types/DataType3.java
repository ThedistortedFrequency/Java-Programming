/*  Data-Type in Java

Primitive: byet, short, char, boolean, int, long, float, double.
primitive data-type inbuilt hota hai.

Non-Primitive: String, Array, Class, Object, Interface
non primitive data type ko khud se banana padta hai.

Java is typed language. we have to declare type.

*/

public class DataType3 {
    public static void main(String[] args) {
        byte a = 8; // 1 byte
        char ch = 'a'; // 2 byte
        boolean var = true; // 1 byte
        double price = 10.5; // 8 byte
        int number = 12; // 4 byte
        long Num = 25; // 8 byte
        float num = 35; // 4 byte
        short n = 240; // 2 byte

        System.out.println(a);
        System.out.println(ch);
        System.out.println(var);
        System.out.println(price);
        System.out.println(number);
        System.out.println(Num);
        System.out.println(num);
        System.out.println(n);

    }

}
